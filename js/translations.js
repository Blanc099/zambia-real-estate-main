// Language management functions
function setLanguage(lang) {
  localStorage.setItem('lang', lang);
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (translations[lang] && translations[lang][key]) {
      if (el.tagName === 'INPUT' && (el.type === 'text' || el.type === 'email')) {
        el.placeholder = translations[lang][key];
      } else if (el.tagName === 'TEXTAREA') {
        el.placeholder = translations[lang][key];
      } else {
        el.textContent = translations[lang][key];
      }
    }
  });
  
  // Update document title
  const titleKey = document.body.getAttribute('data-page-title');
  if (titleKey && translations[lang] && translations[lang][titleKey]) {
    document.title = translations[lang][titleKey];
  }
  
  // Refresh admin listeners if on admin page
  if (typeof refreshAdminListeners === 'function') {
    refreshAdminListeners();
  }
  
  // Update language selector buttons
  updateLanguageButtons(lang);
}

function updateLanguageButtons(currentLang) {
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.remove('active');
    if (btn.getAttribute('data-lang') === currentLang) {
      btn.classList.add('active');
    }
  });
}

function getCurrentLanguage() {
  return localStorage.getItem('lang') || 'en';
}

function t(key) {
  const lang = getCurrentLanguage();
  return translations[lang] && translations[lang][key] ? translations[lang][key] : key;
}

// Initialize language on page load
document.addEventListener('DOMContentLoaded', () => {
  const lang = getCurrentLanguage();
  setLanguage(lang);
});
